package fproject.app.fproject.vo;

public class EmoticongroupVo{
	private int emognum;
	private int num;
	public EmoticongroupVo() {}
	public EmoticongroupVo(int emognum, int num) {
		super();
		this.emognum = emognum;
		this.num = num;
	}
	public int getEmognum() {
		return emognum;
	}
	public void setEmognum(int emognum) {
		this.emognum = emognum;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
}
